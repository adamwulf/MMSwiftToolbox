import Foundation

extension CGSize {
    public init(_ width: CGFloat, _ height: CGFloat) {
        self.init(width: width, height: height)
    }

    public init(_ width: Double, _ height: Double) {
        self.init(width: width, height: height)
    }

    public init(_ width: Int, _ height: Int) {
        self.init(width: width, height: height)
    }
}
